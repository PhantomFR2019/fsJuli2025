//Looping for

for (let i = 0; i < 5; i++) {
    console.log('ini i = ' + i);
}

// Looping while
let j = 0;
while (j < 5) {
    console.log('ini j = ' + j);
    j++;
}

let k = 0;
let kstatus = true;

while (kstatus) {
    console.log('ini k = ' + k);
    k++;
    if (k === 5) {
        kstatus = false;
    }
}

